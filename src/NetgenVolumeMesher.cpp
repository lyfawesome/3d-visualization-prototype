QString NetgenVolumeMesher::findNetgenExecutable()
{
    const QString envPath = qEnvironmentVariable("NETGEN_EXE");
    if (!envPath.isEmpty() && QFile::exists(envPath)) {
        return envPath;
    }

    const QString appLocal = QDir(QCoreApplication::applicationDirPath()).absoluteFilePath("netgen.exe");
    if (QFile::exists(appLocal)) {
        return appLocal;
    }

    const QString projectLocal = QDir(QCoreApplication::applicationDirPath()).absoluteFilePath("../../extern/netgen/bin/netgen.exe");
    if (QFile::exists(projectLocal)) {
        return QDir::cleanPath(projectLocal);
    }

    const QString onPath = QStandardPaths::findExecutable("netgen");
    if (!onPath.isEmpty()) {
        return onPath;
    }

    const QString msys2Netgen = QStringLiteral("C:/msys64/ucrt64/bin/netgen.exe");
    if (QFile::exists(msys2Netgen)) {
        return msys2Netgen;
    }

    const QString envPython = qEnvironmentVariable("NETGEN_PYTHON");
    if (!envPython.isEmpty() && QFile::exists(envPython)) {
        return envPython;
    }

    const QDir appDir(QCoreApplication::applicationDirPath());
    const QString projectPython = appDir.absoluteFilePath("../../extern/netgen/python-env/Scripts/python.exe");
    if (QFile::exists(projectPython)) {
        return QDir::cleanPath(projectPython);
    }

    return QString();
}

VolumeMesh NetgenVolumeMesher::meshStepFile(
    const QString &stepFilePath,
    const QString &workDirectory,
    const QVector<int> &constraintFaceIds,
    const QString &netgenExecutable,
    const QString &netgenOptions)
{
    const QString netgenPath = netgenExecutable.isEmpty() ? findNetgenExecutable() : netgenExecutable;
    if (netgenPath.isEmpty()) {
        throw std::runtime_error("Netgen backend was not found. Run scripts/download_netgen_windows.ps1, set NETGEN_EXE, or set NETGEN_PYTHON.");
    }

    QDir workDir(workDirectory);
    if (!workDir.exists() && !workDir.mkpath(QStringLiteral("."))) {
        throw std::runtime_error("Could not create Netgen working directory.");
    }

    const QString baseName = QFileInfo(stepFilePath).completeBaseName().isEmpty()
        ? QStringLiteral("model")
        : QFileInfo(stepFilePath).completeBaseName();
    const QString volPath = workDir.absoluteFilePath(baseName + QStringLiteral("_netgen.vol"));
    runNetgen(netgenPath, stepFilePath, volPath, workDir.absolutePath(), netgenOptions);

    NetgenMesh netgenMesh = readNetgenVolFile(volPath);
    if (netgenMesh.tetrahedra.isEmpty()) {
        throw std::runtime_error("Netgen .vol did not contain tetrahedral volume elements.");
    }

    const QSet<int> constraintFaceIdSet(constraintFaceIds.begin(), constraintFaceIds.end());
    remapNetgenSurfaceMarkersFromSelectedFaces(stepFilePath, netgenMesh, constraintFaceIdSet);
    validateConstraintMarkers(netgenMesh, constraintFaceIdSet);
    VolumeMesh mesh = convertNetgenMesh(netgenMesh, constraintFaceIdSet);
    mesh.nodePath = volPath;
    mesh.elementPath = volPath;
    mesh.vtkPath = workDir.absoluteFilePath(baseName + QStringLiteral("_netgen.vtk"));
    mesh.boundaryVtkPath = workDir.absoluteFilePath(baseName + QStringLiteral("_netgen.faces.vtk"));
    writeVolumeLegacyVtkFile(mesh.vtkPath, mesh);
    writeBoundaryLegacyVtkFile(mesh.boundaryVtkPath, mesh);
    return mesh;
}
